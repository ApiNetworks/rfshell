using System;
using System.Collections.Generic;
using System.Linq;
using System.Reflection;

namespace RFShell
{
    public class IconProp
    {
        public string IconName { get; set; }
    }

    public static class IconPropExtension
    {
        public static IconProp RandomIcon(this IconProp entity)
        {
            List<FieldInfo> fields = GetConstants(typeof(IconNames));
            var rnd = new Random();
            return new IconProp
            {
                IconName = fields.OrderBy(s => rnd.NextDouble()).First().GetValue(null).ToString()
            };
        }
        private static List<FieldInfo> GetConstants(Type type)
        {
            FieldInfo[] fieldInfos = type.GetFields(BindingFlags.Public | BindingFlags.Static | BindingFlags.FlattenHierarchy);
            return fieldInfos.Where(fi => fi.IsLiteral && !fi.IsInitOnly).ToList();
        }
    }
}

